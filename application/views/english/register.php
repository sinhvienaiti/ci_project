Đăng ký
-	Bạn phải đăng ký một tài khoản Manager từ công ty HappyKids để sử dụng dịch vụ Manager.

Hướng dẫn
-	Manager có thể thúc đẩy việc học tiếng Anh của học viên bằng cách quản lí thời gian học và trao đổi với học viên qua dịch vụ này.
